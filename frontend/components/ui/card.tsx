import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "elevated" | "outline" | "ghost"
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variants = {
    default: "rounded-xl border bg-card text-card-foreground shadow-sm",
    elevated: "rounded-xl border bg-card text-card-foreground shadow-lg hover:shadow-xl transition-all duration-300",
    outline: "rounded-xl border-2 bg-transparent text-card-foreground",
    ghost: "rounded-xl bg-card/50 text-card-foreground backdrop-blur-sm"
  }
  
  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

